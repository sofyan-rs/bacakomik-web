<h1>Kebijakan Privasi</h1>

<p>Aplikasi BacaKomik dimiliki oleh KoiDevz, yang bertanggung jawab atas data pribadi Anda.</p>

<p>Kami telah mengadopsi Kebijakan Privasi ini untuk menjelaskan cara kami memproses informasi yang dikumpulkan oleh KoiDevz, serta alasan di balik kebutuhan kami untuk mengumpulkan data pribadi tertentu tentang Anda. Oleh karena itu, disarankan untuk membaca Kebijakan Privasi ini sebelum menggunakan website Manhwaindo.id.</p>

<p>Kami sangat memperhatikan kerahasiaan dan keamanan data pribadi Anda.</p>

<strong>Informasi pribadi yang kami kumpulkan:</strong>
<p>Ketika menggunakan BacaKomik, kami secara otomatis mengumpulkan beberapa informasi tentang perangkat Anda, seperti alamat IP, zona waktu, dan cookie yang ada di perangkat Anda. Selain itu, saat Anda menjelajahi Aplikasi, kami juga mengumpulkan informasi tentang halaman atau produk yang Anda lihat, istilah pencarian yang membawa Anda ke Aplikasi, dan bagaimana Anda berinteraksi dengan Aplikasi. Kami menyebut informasi ini sebagai "Informasi Perangkat". Selain itu, saat Anda mendaftar, kami mungkin juga mengumpulkan data pribadi seperti nama, alamat, dan informasi pembayaran.</p>

<strong>Tujuan pemrosesan data Anda:</strong>
<p>Prioritas kami adalah menjaga keamanan data pelanggan. Kami hanya memproses sejumlah kecil data pengguna yang benar-benar diperlukan untuk menjalankan aplikasi. Informasi yang dikumpulkan secara otomatis digunakan untuk mendeteksi kemungkinan penyalahgunaan dan membuat statistik penggunaan aplikasi. Statistik ini tidak mencakup informasi pribadi yang dapat mengidentifikasi pengguna tertentu.</p>

<p>Anda dapat mengunjungi aplikasi tanpa memberikan identitas atau informasi pribadi, namun untuk menggunakan beberapa fitur atau menerima newsletter kami, Anda perlu memberikan informasi pribadi. Anda dapat memilih untuk tidak memberikan informasi pribadi, tetapi beberapa fitur mungkin tidak tersedia.</p>

<strong>Hak-hak Anda:</strong>
<p>Jika Anda warga Eropa, Anda memiliki hak-hak terkait data pribadi Anda, termasuk hak atas penjelasan, akses, perbaikan, penghapusan, pembatasan pemrosesan, portabilitas data, dan penolakan terhadap pengambilan keputusan otomatis.</p>

<strong>Kontak:</strong>
